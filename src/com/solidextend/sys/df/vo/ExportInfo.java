package com.solidextend.sys.df.vo;

public class ExportInfo {

	private String id;
	private String fields;
	private String fieldDescs;
	private int fileType = 0;
	private int title = 0;
	private int autoPrimary= 1;
	private String fileName;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getFields() {
		return fields;
	}
	public void setFields(String fields) {
		this.fields = fields;
	}
	public int getFileType() {
		return fileType;
	}
	public void setFileType(int fileType) {
		this.fileType = fileType;
	}
	public int getTitle() {
		return title;
	}
	public void setTitle(int title) {
		this.title = title;
	}
	public String getFieldDescs() {
		return fieldDescs;
	}
	public void setFieldDescs(String fieldDescs) {
		this.fieldDescs = fieldDescs;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public int getAutoPrimary() {
		return autoPrimary;
	}
	public void setAutoPrimary(int autoPrimary) {
		this.autoPrimary = autoPrimary;
	}	
}
